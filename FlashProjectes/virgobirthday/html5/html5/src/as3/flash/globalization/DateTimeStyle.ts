module flash
{
    export class DateTimeStyle
    {

        /**[静态] 指定使用自定义样式字符串来指定日期或时间样式。*/
        static CUSTOM : string = "custom";
            
        /** [静态] 指定长型样式的日期或时间。*/
        static LONG : string = "long";
           
        /** [静态] 指定中型样式的日期或时间。*/
        static MEDIUM : string = "medium";
           
        /** [静态] 指定设置了格式的字符串中不应包含日期或时间。*/
        static NONE : string = "none";
           
        /**[静态] 指定短型样式的日期或时间。*/
        static SHORT : string = "short";

        constructor()
        {

        }
            
    }
}